    private void formMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMousePressed
        switch (state) {
            case 0:
                break;
            case 1:
                theController.theWorld.sourceClick(evt.getX(), evt.getY());
                repaint();
                break;
            case 2:
                theController.theWorld.vehicleClick(evt.getX(), evt.getY(), Math.random() * (2 * Math.PI), choice);
                repaint();
                break;
        }
    }//GEN-LAST:event_formMousePressed

