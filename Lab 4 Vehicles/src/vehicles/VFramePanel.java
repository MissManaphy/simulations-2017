    private void setOptions() {
        intensitySlider.setMinimum(10);
        intensitySlider.setMaximum(200000);
        intensitySlider.setMinorTickSpacing(20000);
        intensitySlider.setPaintTicks(true);
        
        delaySlider.setMinimum(10);
        delaySlider.setMaximum(200);
        delaySlider.setMinorTickSpacing(10);
        delaySlider.setPaintTicks(true);

        doneButton.setVisible(false);
        intensitySlider.setVisible(false);
        intensityLabel.setVisible(false);

        loveVehicle.setVisible(false);
        fearVehicle.setVisible(false);
        explorerVehicle.setVisible(false);
        agroVehicle.setVisible(false);

        state = NOTHING;
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));

    }

